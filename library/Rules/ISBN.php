namespace Respect\Validation\Rules;

class ISBN extends AbstractRule
{
    public function validate($input)
    {
        // https://howtodoinjava.com/regex/java-regex-validate-international-standard-book-number-isbns/
        $regex =  "^(?:ISBN(?:-1[03])?:? )?(?=[0-9X]{10}$|(?=(?:[0-9]+[- ]){3})[- 0-9X]{13}$|97[89][0-9]{10}$|(?=(?:[0-9]+[- ]){4})[- 0-9]{17}$)(?:97[89][- ]?)?[0-9]{1,5}[- ]?[0-9]+[- ]?[0-9]+[- ]?[0-9X]$";
        return \preg_match($regex, $input);
    }
}
